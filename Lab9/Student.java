package Lab9;

public class Student {

	private int id;
	private String name;
	private int year;

	public Student(int id, String name, int year) {
		this.id = id;
		this.name = name;
		this.year = year;
	}

	// Constructor and other methods

	// Getter methods
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getYear() {
		return year;
	}

}
